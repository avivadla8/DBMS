CREATE TABLE courses(cid integer,name VARCHAR(30),credits integer,PRIMARY KEY(cid));
CREATE TABLE student_courses(sid integer, cid integer, grade VARCHAR(3),absents integer,FOREIGN KEY (sid) REFERENCES students(sid),FOREIGN KEY (cid) REFERENCES courses(cid));
CREATE TABLE grade_int(grade VARCHAR(3),points integer);

